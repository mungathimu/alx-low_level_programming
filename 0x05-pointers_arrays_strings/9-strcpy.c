/**
 * _strcpy - copies string pointed to by src and including the null terminator
 * @dest: Where string will be copied to
 * @src: Holds the string
 * Return: dest
 */

char *_strcpy(char *dest, char *src)
{
	int i;

	for (i = 0 ; src[i] !=  '\0' ; i++)
		dest[i] = src[i];
	return (dest);



























}
